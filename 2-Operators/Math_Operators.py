#   +
print(2+4+6+7+8+9)
#   -
print(120-30-40)
#   *
pi = 3.1415927
r = 4
cevre = 2*pi*r
print(cevre)
#   /
print(10/2, -10/3, 5/4)
#   %
print(10%2, -10%3, 5%4)
#   //
print(10//2, -10//3, 5//4)
#   **
print(5**1, 5**2, 5**3, 5**4)
#   min() ve max() Fonksiyonları en düşük veya en yüksek değeri bulmak için kullanılır.
x = min(5, 25, 125, 625)
y = max(625, 125, 25, 5)
print(x)
print(y)
